static void CPU_DISABLE_ALL_INTERRUPTS(void)
{ 
		__asm("MOV R0, 0x1\n");
		__asm("MSR PRIMASK, R0\n");
}
																				 
static void CPU_ENABLE_ALL_INTERRUPTS(void)
{
		__asm("MOV R0, 0x0\n");
		__asm("MSR PRIMASK, R0\n");		
}			
 
